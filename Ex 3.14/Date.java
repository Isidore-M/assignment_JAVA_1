
public class Date{

     private int Year;
     private int Month;
     private int Day;

     public void setYear (int Year) {
        this.Year = Year;
    }
     public void setMonth (int Month ) {
        this.Month = Month;
    }
     public void setDay (int Day ) {
        this.Day = Day;
    }

     public int getDay () {
        return Day;
    }
     public int getMonth () {
        return Month;
    }
     public int getYear () {
        return Year;
    }
}